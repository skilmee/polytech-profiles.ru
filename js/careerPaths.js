function flattenProfiles() {
  const list = [];

  programs.forEach(program => {
    program.profiles.forEach(profile => {
      list.push({
        code: program.code,
        programName: program.name,
        profile
      });
    });
  });

  return list;
}

const allCareerProfiles = flattenProfiles();

function populateCareerSelect() {
  if (!careerProfileSelect) return;

  careerProfileSelect.innerHTML = allCareerProfiles.map((item, index) => `
    <option value="${index}">${item.code} — ${item.profile.name}</option>
  `).join("");
}

function getCareerData(profileName) {
  return CAREER_PATHS[profileName] || {
    summary: "Для этого профиля пока не добавлена подробная карьерная траектория.",
    start: ["Стартовая роль уточняется"],
    growth: ["Направление роста уточняется"],
    longTerm: ["Долгосрочная траектория уточняется"],
    important: ["Карьерные требования уточняются"]
  };
}

function buildCareerList(items) {
  return items.map(item => `<li>${item}</li>`).join("");
}

function renderCareerProfile(index) {
  const selected = allCareerProfiles[Number(index)];
  if (!selected || !careerResult) return;

  const careerData = getCareerData(selected.profile.name);

  careerResult.innerHTML = `
    <div class="career-hero-card">
      <div class="career-profile-label">${selected.code} — ${selected.programName}</div>
      <h2>${selected.profile.name}</h2>
      <p>${careerData.summary}</p>
    </div>

    <div class="career-path-grid">
      <article class="career-stage-card">
        <div class="career-stage-number">1</div>
        <h3>Старт после обучения</h3>
        <ul>${buildCareerList(careerData.start)}</ul>
      </article>

      <article class="career-stage-card">
        <div class="career-stage-number">2</div>
        <h3>Следующий этап роста</h3>
        <ul>${buildCareerList(careerData.growth)}</ul>
      </article>

      <article class="career-stage-card">
        <div class="career-stage-number">3</div>
        <h3>Долгосрочная траектория</h3>
        <ul>${buildCareerList(careerData.longTerm)}</ul>
      </article>
    </div>

    <div class="career-info-grid">
      <article class="career-info-card">
        <h3>Что особенно важно для этой траектории</h3>
        <ul>${buildCareerList(careerData.important)}</ul>
      </article>

      <article class="career-info-card">
        <h3>Профиль подготовки</h3>
        <p>${selected.profile.description || "Описание профиля не указано."}</p>
        <div class="career-info-meta">
          <span class="career-badge">Программирование: ${selected.profile.rating?.programming ?? "—"}/10</span>
          <span class="career-badge">Математика: ${selected.profile.rating?.math ?? "—"}/10</span>
          <span class="career-badge">Инженерность: ${selected.profile.rating?.engineering ?? "—"}/10</span>
          <span class="career-badge">Прикладность: ${selected.profile.rating?.applied ?? "—"}/10</span>
          <span class="career-badge">Исследовательский потенциал: ${selected.profile.rating?.research ?? "—"}/10</span>
        </div>
      </article>
    </div>
  `;
}

populateCareerSelect();

if (careerProfileSelect) {
  careerProfileSelect.addEventListener("change", event => {
    renderCareerProfile(event.target.value);
  });

  careerProfileSelect.value = "0";
  renderCareerProfile("0");
}